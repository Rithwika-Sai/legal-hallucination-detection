# ─── PICK YOUR CORRECTOR MODEL ──────────────────────────────────────────────
MODEL_ENGINE = "mistral"          # "mistral"  or  "falcon"

MODEL_IDS = {
    "mistral": "mistralai/Mistral-7B-Instruct-v0.2",
    #"falcon":  "tiiuae/falcon-7b-instruct",
}
assert MODEL_ENGINE in MODEL_IDS, f"MODEL_ENGINE must be one of {list(MODEL_IDS)}"
MODEL_ID = MODEL_IDS[MODEL_ENGINE]

# ─── CONFIGURATION ──────────────────────────────────────────────────────────
EMBED_MODEL = "all-MiniLM-L6-v2"
HALL_PATH   = "/content/hall_sent_clean.csv"
SRC_PATH    = "/content/test.json"
OUT_PATH    = f"outputs/basic_rag_{MODEL_ENGINE}_mitigated.csv"
TOP_K       = 3 if MODEL_ENGINE == "mistral" else 2     # Falcon has a 2048 window
MAX_CTX     = 4096 if MODEL_ENGINE == "mistral" else 2048
GEN_TEMP    = 0.1 if MODEL_ENGINE == "mistral" else 0.2
DEVICE      = "cuda" if torch.cuda.is_available() else "cpu"

INSTRUCTION = ("You are correcting a sentence from a legal-case summary so it is faithful "
               "to the source judgment. Use ONLY the context below. Rewrite the sentence to "
               "be factually accurate; drop or fix any claim not supported by the context. "
               "Reply with the corrected sentence only.")

def build_prompt(context, sentence):
    """Engine-specific instruction wrapper."""
    if MODEL_ENGINE == "mistral":
        return f"<s>[INST] {INSTRUCTION}\n\nContext:\n{context}\n\nSentence to correct: {sentence} [/INST]"
    return (f"{INSTRUCTION}\n\nContext:\n{context}\n\n"
            f"Sentence to correct: {sentence}\n\nCorrected sentence:")

# ─── 1. LOAD TARGETS (hallucinated) + TRUTH (source judgments) ──────────────
print(f"[1/4] Engine={MODEL_ENGINE} | loading {HALL_PATH} and {SRC_PATH} ...")
hall_rows = load_hall_sentences(HALL_PATH)
records   = load_source_records(SRC_PATH)
print(f"      {len(hall_rows)} hallucinated sentences | {len(records)} source judgments")

# ─── 2. EMBEDDER + EVIDENCE RETRIEVAL ───────────────────────────────────────
print("[2/4] Loading embedder ...")
embedder = SentenceTransformer(EMBED_MODEL, device=DEVICE)
_pool_cache = {}

def retrieve_evidence(query, record, k=TOP_K):
    sid = record.get("id")
    if sid not in _pool_cache:
        pool = split_sentences(record.get("judgment", "")) or [record.get("judgment", "")]
        emb  = embedder.encode(pool, convert_to_numpy=True, normalize_embeddings=True)
        _pool_cache[sid] = (pool, emb)
    pool, emb = _pool_cache[sid]
    q = embedder.encode([query], convert_to_numpy=True, normalize_embeddings=True)[0]
    idx = (emb @ q).argsort()[::-1][:k]
    return [pool[i] for i in idx]

# ─── 3. LOAD CORRECTOR LLM ──────────────────────────────────────────────────
print(f"[3/4] Loading {MODEL_ID} on {DEVICE} ...")
tokenizer = AutoTokenizer.from_pretrained(MODEL_ID)
tokenizer.pad_token = tokenizer.eos_token
model = AutoModelForCausalLM.from_pretrained(
    MODEL_ID,
    torch_dtype=torch.float16 if DEVICE == "cuda" else torch.float32,
    device_map="auto" if DEVICE == "cuda" else None,
    low_cpu_mem_usage=True,
)

def correct_sentence(bad_sentence, evidence):
    context = "\n".join(f"- {e}" for e in evidence)
    prompt  = build_prompt(context, bad_sentence)
    inputs  = tokenizer(prompt, return_tensors="pt", truncation=True, max_length=MAX_CTX).to(model.device)
    with torch.no_grad():
        out = model.generate(**inputs, max_new_tokens=120, do_sample=True, temperature=GEN_TEMP,
                             top_p=0.9, pad_token_id=tokenizer.eos_token_id)
    text = tokenizer.decode(out[0][inputs["input_ids"].shape[1]:], skip_special_tokens=True)
    return text.strip().strip('"')

# ─── 4. CORRECT EACH HALLUCINATED SENTENCE ──────────────────────────────────
print("[4/4] Correcting sentences ...\n")
results = []
for i, r in enumerate(hall_rows, 1):
    rec      = pick_record(r, records)
    evidence = retrieve_evidence(r["sentence"], rec, k=TOP_K)
    fixed    = correct_sentence(r["sentence"], evidence)
    print(f"[{i}/{len(hall_rows)}] {r['model']}/{r['position']}  (source id {rec.get('id')})")
    print(f"   ORIG : {r['sentence']}")
    print(f"   FIXED: {fixed}\n")
    results.append({
        "model": r["model"], "position": r["position"], "source_id": rec.get("id"),
        "original_sentence": r["sentence"], "corrected_sentence": fixed,
    })

save_mitigated(OUT_PATH, results)

